uint32_t prev_capture = 0;
uint32_t current_capture = 0;
uint32_t delta_counts = 0;
float time_between_events = 0.0f;
float debug_array[10] = {0};
uint8_t debug_array_index = 0;

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM2 && htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
    {
        current_capture = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
        if (current_capture >= prev_capture)
        {
            delta_counts = current_capture - prev_capture;
        }
        else
        {
            delta_counts = (0xFFFFFFFF - prev_capture) + current_capture;
        }
        time_between_events = delta_counts * (1.0f / 84000000.0f); // Time in seconds
        prev_capture = current_capture;
        // Example: Print or store time_between_events
        debug_array[debug_array_index++] = time_between_events;
    }
}
